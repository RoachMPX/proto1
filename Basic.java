package simple;

public class Basic {

	public double plus(double x, double y) {
		return x + y;
	}

	public double min(double x, double y) {
		return x - y;
	}

	public double times(double x, double y) {
		return x * y;
	}

	public double div(double x, double y) {
		return x / y;
	}
}
